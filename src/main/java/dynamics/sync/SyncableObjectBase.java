package dynamics.sync;

public abstract class SyncableObjectBase implements ISyncableObject {

    protected boolean dirty = false;

    @Override
    public boolean isDirty() {
        return dirty;
    }

    @Override
    public void markClean() {
        dirty = false;
    }

    @Override
    public void markDirty() {
        dirty = true;
    }
}